package com.VDemianenko.Task_8;
// Программа определяющая четное или нечетное число
public class Task_8 {
    public static void main(String[] args) {
        int Number_1 = 29;

        if ((Number_1 % 2) == 0) {
            System.out.println("Число " + Number_1 + " является чётным");
        } else {
            System.out.println("Число " + Number_1 + " является нечётным");
        }
    }
}
